package problemas;

public class Elemento_dicionario {
	
	
	private String valor;
	private int chave;
	
	public Elemento_dicionario(String v, int c) {
		valor = v;
		chave = c;
	}

	public String getValor() {
		return valor;
	}

	public void setValor(String valor) {
		this.valor = valor;
	}

	public int getChave() {
		return chave;
	}

	public void setChave(int chave) {
		this.chave = chave;
	}
	
	
	
	

}
